BANK0:

    BCF STATUS,IRP
    BCF STATUS,RP0
    BCF STATUS,RP1
    
    RETURN
    
BANK1:

    BCF STATUS,IRP
    BCF STATUS,RP1
    BSF STATUS,RP0
    
    RETURN
    
BANK2:

    BSF STATUS,IRP
    BSF STATUS,RP1
    BCF STATUS,RP0
    
    RETURN 
    
BANK3:

    BSF STATUS,IRP
    BSF STATUS,RP1
    BSF STATUS,RP0
    
    RETURN   

INITDELAY:          ;INIT DELAY 2ms

    movlw 0X20          
    movwf 0x40         
    
DELAY0:
    
    DECFSZ 0x40,f
    GOTO DELAY0
    
    RETURN
        
