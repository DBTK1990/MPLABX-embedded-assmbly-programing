   LIST P=PIC16F877
#include <P16f877.inc>

LCD CODE 0x9F
INITDisplay:
   
   ;CALL BANK1 
   BANKSEL TRISD                  
   CLRF TRISD                  
   MOVLW 0X06                  
   MOVWF ADCON1                ;SET ALL TO D
   CLRF TRISE
   ;CALL BANK0
   BANKSEL PORTD
   MOVLW 0X30                  ;RESET SCREEN
   
   CALL FUNCTION.LCD.WRITE
   CALL INITDELAY
   MOVLW 0X30
   
   CALL FUNCTION.LCD.WRITE
   CALL INITDELAY
   MOVLW 0X30
   
   CALL FUNCTION.LCD.WRITE
   CALL INITDELAY              ;END SCREEN RESET
   MOVLW 0X38                  ;SET CONFIG. FOR SCREEN
   
   CALL FUNCTION.LCD.WRITE
   MOVLW 0X0C                  ;DISPLAY=ON BIT.D=1
                    
   CALL FUNCTION.LCD.WRITE
   MOVLW 0X01                  ;DISPLAY CLEAR
   
   CALL FUNCTION.LCD.WRITE 
   MOVLW 0X06                  ;DISPLAY SET L.T.R
   
   CALL FUNCTION.LCD.WRITE
   
   RETURN

 GLOBAL INITDisplay
   
FUNCTION.LCD.WRITE:            ;FUNCTION WRITE TO LCD

 MOVWF PORTD   
 BCF PORTE,RE1
 BSF PORTE,RE0
 CALL INITDELAY
 BCF PORTE,RE0
    
    RETURN

 
    
VAL.LCD.WRITE:                 ;VAL WRITE ON SCREEN

 MOVWF PORTD   
 BSF PORTE,RE1
 BSF PORTE,RE0
 CALL INITDELAY
 BCF PORTE,RE0
    
   RETURN    

;-----------------255.function------------------------------;   
PRINT.255:
        
   MOVLW 0X86
   CALL FUNCTION.LCD.WRITE
   CALL ASCII.NUMBER.255
   
   MOVLW 0X03
   MOVWF 0X48
   
   MOVLW 0X52
   MOVWF FSR
   
LOOP.3:   
   
   MOVFW INDF
   
   CALL VAL.LCD.WRITE
   DECF FSR,F
   DECFSZ 0X48,F
   GOTO LOOP.3
   
   RETURN   

 GLOBAL PRINT.255   

ASCII.NUMBER.255:
   
   MOVLW 0X03
   MOVWF 0X48
   
   MOVLW 0X52
   MOVWF FSR
   
   MOVLW 0X30
   
LOOP.ASCII.255:
   
   ADDWF INDF,F    
   DECF FSR,F
   DECFSZ 0X48,F
   GOTO LOOP.ASCII.255
   
   RETURN
